def check_grid(grid):
    for row in grid:
        for i in range (len(row)-1):
            if row[i]==row[i+1]:
                return False
    rotated_grid = list(zip(*grid))
    for row2 in rotated_grid:
        for i in range (len(row2)-1):
            if row2[i]==row2[i+1]:
                return False
    return True



